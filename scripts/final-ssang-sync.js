async function syncShinSangMoon() {
    const lines = rawData.trim().split('\n');
    const matches = lines.map(line => {
        const [date, oppRaw, result, map, note] = line.split('|').map(s => s.trim());
        const isWin = result === '승';
        return {
            player_name: '신상문',
            match_date: date,
            opponent_name: oppRaw.split('(')[0],
            is_win: isWin,
            result_text: result,
            map: map,
            note: note,
            gender: 'male'
        };
    });

    console.log(`🚀 [신상문] 대표님의 정밀 데이터 ${matches.length}건 박제 작전 개시!`);

    try {
        // 1. 기존 2025년 이후 데이터 싹 비우기
        await supabase.from('eloboard_matches')
            .delete()
            .eq('player_name', '신상문')
            .gte('match_date', '2025-01-01');

        // 2. 새로운 정밀 데이터 삽입
        const { error } = await supabase.from('eloboard_matches').insert(matches);
        if (error) throw error;

        console.log(`🏆 성공! 신상문 선수의 역사적 2025~2026 전적 ${matches.length}건이 완벽하게 복원되었습니다.`);
    } catch (e) {
        console.error(`🚨 작전 실패: ${e.message}`);
    }
}

